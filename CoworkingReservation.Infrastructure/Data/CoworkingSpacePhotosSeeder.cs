        public static async Task SeedCoworkingSpacePhotosAsync(ApplicationDbContext context)
        {
            Console.WriteLine("ðŸ“¸ Iniciando seeder de CoworkingSpacePhotos...");

            try
            {
                // Verificar si ya existen fotos
                var existingCount = await context.Database.SqlQueryRaw<int>(
                    "SELECT COUNT(*) as Value FROM CoworkingSpacePhotos").FirstAsync();

                if (existingCount > 0)
                {
                    Console.WriteLine($"âœ… La tabla CoworkingSpacePhotos ya tiene {existingCount} registros.");
                    return;
                }

                // Verificar que tengamos exactamente 1500 URLs
                if (PhotoUrls.Length != 1500)
                {
                    Console.WriteLine($"âŒ Error: Se esperaban 1500 URLs pero se encontraron {PhotoUrls.Length}");
                    return;
                }

                // Verificar que existan los 300 CoworkingSpaces
                var coworkingSpacesCount = await context.CoworkingSpaces.CountAsync(cs => cs.Id >= 1 && cs.Id <= 300);
                if (coworkingSpacesCount < 300)
                {
                    Console.WriteLine("âŒ No se encontraron suficientes CoworkingSpaces (necesarios: 300).");
                    return;
                }

                Console.WriteLine("ðŸ“· Generando 1500 fotos para 300 CoworkingSpaces...");

                var insertStatements = new List<string>();
                var uploadDate = DateTime.UtcNow;

                // Para cada CoworkingSpace (1-300)
                for (int coworkingSpaceId = 1; coworkingSpaceId <= 300; coworkingSpaceId++)
                {
                    // Tomar 5 fotos consecutivas del array
                    int startIndex = (coworkingSpaceId - 1) * 5;

                    for (int photoIndex = 0; photoIndex < 5; photoIndex++)
                    {
                        int urlIndex = startIndex + photoIndex;
                        string filePath = PhotoUrls[urlIndex];

                        // Extraer el nombre del archivo de la URL
                        string fileName = filePath.Split('/').Last();

                        // La primera foto de cada espacio serÃ¡ la foto de portada
                        bool isCoverPhoto = photoIndex == 0;

                        string uploadedAtString = uploadDate.ToString("yyyy-MM-dd HH:mm:ss.fff");

                        insertStatements.Add($"('{filePath}', '{fileName}', 'image/jpeg', '{uploadedAtString}', {(isCoverPhoto ? "1" : "0")}, {coworkingSpaceId})");
                    }
                }

                Console.WriteLine($"ðŸ“ Preparando inserciÃ³n de {insertStatements.Count} fotos...");

                // Insertar por lotes de 50 para evitar problemas de memoria
                var batchSize = 50;
                var totalBatches = (int)Math.Ceiling((double)insertStatements.Count / batchSize);

                for (int batch = 0; batch < totalBatches; batch++)
                {
                    var batchStatements = insertStatements
                        .Skip(batch * batchSize)
                        .Take(batchSize)
                        .ToList();

                    var valuesClause = string.Join(", ", batchStatements);
                    var sql = $"INSERT INTO CoworkingSpacePhotos (FilePath, FileName, MimeType, UploadedAt, IsCoverPhoto, CoworkingSpaceId) VALUES {valuesClause}";

                    await context.Database.ExecuteSqlRawAsync(sql);

                    Console.WriteLine($"   Lote {batch + 1}/{totalBatches} insertado ({batchStatements.Count} fotos)");
                }

                Console.WriteLine("ðŸŽ‰ Â¡CoworkingSpacePhotos seeding completado exitosamente!");

                // Mostrar estadÃ­sticas finales
                var finalCount = await context.Database.SqlQueryRaw<int>(
                    "SELECT COUNT(*) as Value FROM CoworkingSpacePhotos").FirstAsync();
                var coverPhotos = await context.Database.SqlQueryRaw<int>(
                    "SELECT COUNT(*) as Value FROM CoworkingSpacePhotos WHERE IsCoverPhoto = 1").FirstAsync();

                Console.WriteLine($"ðŸ“Š Total de fotos creadas: {finalCount}");
                Console.WriteLine($"ðŸ“Š Fotos de portada: {coverPhotos}");
                Console.WriteLine($"ðŸ“Š Fotos regulares: {finalCount - coverPhotos}");
            }
            catch (Exception ex)
            {
                Console.WriteLine($"âŒ Error en CoworkingSpacePhotosSeeder: {ex.Message}");
                Console.WriteLine($"ðŸ” Stack trace: {ex.StackTrace}");
                throw;
            }
        }
